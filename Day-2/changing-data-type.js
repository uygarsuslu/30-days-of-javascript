// ********** CHANGING DATA TYPE ********** //

// *** String to Int *** //
let num1 = '10'
let numInt = parseInt(num1)
console.log("parseInt:", numInt) // 10

// *** String to Float *** //
let num2 = '9.81'
let numFloat = parseFloat(num2)
console.log(numFloat) // 9.81
